    {
       
        $employe=new Employe();
        $values=$request->all();
        $employe->fill($values);
        $endereco=new Endereco();
        $endereco->fill($values);
        $service =new EmployeService();
       $result= $service->save_employe($employe,$endereco);
        $status=$result['status'];
        $message=$result['message'];
        return back()->with($status,$message);

    }

